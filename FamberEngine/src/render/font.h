static const char* T_VERT =
    "#version 120\n"
    "attribute vec2 aPos; attribute vec2 aUV; attribute vec4 aCol;\n"
    "uniform vec2 uScreen;\n"
    "varying vec2 vUV; varying vec4 vCol;\n"
    "void main(){ vUV=aUV; vCol=aCol;\n"
    "  gl_Position=vec4(aPos.x/uScreen.x*2.0-1.0, 1.0-aPos.y/uScreen.y*2.0, 0.0, 1.0); }\n";

static const char* T_FRAG =
    "#version 120\n"
    "uniform sampler2D uTex;\n"
    "varying vec2 vUV; varying vec4 vCol;\n"
    "void main(){ gl_FragColor = vCol * texture2D(uTex, vUV); }\n";

// atlas: 16x6 cells of 8x8; cells 0..94 = ASCII 32..126, cell 95 = solid white
struct TextGL {
    static const int AW = 128, AH = 48;
    GLuint prog = 0, tex = 0, vbo = 0;
    GLint aPos = -1, aUV = -1, aCol = -1, uScreen = -1, uTex = -1;
    std::vector<float> v; // x,y,u,v,r,g,b,a
    int sw = 1, sh = 1;

    static GLuint compile(GLenum type, const char* src) {
        GLuint s = glCreateShader(type);
        glShaderSource(s, 1, &src, nullptr);
        glCompileShader(s);
        GLint ok = 0; glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
        if (!ok) { char log[1024]; glGetShaderInfoLog(s, 1024, nullptr, log); printf("[font] %s\n", log); }
        return s;
    }

    void init() {
        GLuint vs = compile(GL_VERTEX_SHADER, T_VERT), fs = compile(GL_FRAGMENT_SHADER, T_FRAG);
        prog = glCreateProgram();
        glAttachShader(prog, vs); glAttachShader(prog, fs); glLinkProgram(prog);
        glDeleteShader(vs); glDeleteShader(fs);
        aPos = glGetAttribLocation(prog, "aPos");
        aUV = glGetAttribLocation(prog, "aUV");
        aCol = glGetAttribLocation(prog, "aCol");
        uScreen = glGetUniformLocation(prog, "uScreen");
        uTex = glGetUniformLocation(prog, "uTex");

        std::vector<unsigned char> px(AW * AH * 4, 0);
        for (int c = 0; c < 95; c++) {
            int cx = (c % 16) * 8, cy = (c / 16) * 8;
            for (int row = 0; row < 8; row++)
                for (int col = 0; col < 8; col++)
                    if ((FONT8x8[c][row] >> col) & 1) {
                        int o = ((cy + row) * AW + cx + col) * 4;
                        px[o] = px[o + 1] = px[o + 2] = px[o + 3] = 255;
                    }
        }
        for (int row = 0; row < 8; row++) // cell 95: solid white for rects
            for (int col = 0; col < 8; col++) {
                int o = ((40 + row) * AW + 120 + col) * 4;
                px[o] = px[o + 1] = px[o + 2] = px[o + 3] = 255;
            }
        glGenTextures(1, &tex);
        glBindTexture(GL_TEXTURE_2D, tex);
        glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, AW, AH, 0, GL_RGBA, GL_UNSIGNED_BYTE, px.data());
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
        glGenBuffers(1, &vbo);
    }

    void begin(int w, int h) { sw = w > 0 ? w : 1; sh = h > 0 ? h : 1; v.clear(); }

    void quad(float x, float y, float w, float h, float u0, float v0, float u1, float v1,
              float r, float g, float b, float a) {
        float q[6][4] = {{x, y, u0, v0}, {x + w, y, u1, v0}, {x + w, y + h, u1, v1},
                         {x, y, u0, v0}, {x + w, y + h, u1, v1}, {x, y + h, u0, v1}};
        for (int i = 0; i < 6; i++) {
            v.push_back(q[i][0]); v.push_back(q[i][1]); v.push_back(q[i][2]); v.push_back(q[i][3]);
            v.push_back(r); v.push_back(g); v.push_back(b); v.push_back(a);
        }
    }

    void rect(float x, float y, float w, float h, float r, float g, float b, float a) {
        float u = 124.5f / AW, vv = 44.5f / AH; // center of the solid cell
        quad(x, y, w, h, u, vv, u, vv, r, g, b, a);
    }

    void text(float x, float y, int scale, const char* s, float r, float g, float b, float a) {
        for (; *s; s++) {
            unsigned char c = (unsigned char)*s;
            if (c >= 32 && c <= 126) {
                int i = c - 32, cx = (i % 16) * 8, cy = (i / 16) * 8;
                quad(x, y, 8.0f * scale, 8.0f * scale,
                     cx / (float)AW, cy / (float)AH, (cx + 8) / (float)AW, (cy + 8) / (float)AH,
                     r, g, b, a);
            }
            x += 8.0f * scale;
        }
    }

    void draw() {
        if (v.empty()) return;
        glDisable(GL_DEPTH_TEST);
        glEnable(GL_BLEND);
        glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
        glUseProgram(prog);
        glUniform2f(uScreen, (float)sw, (float)sh);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, tex);
        glUniform1i(uTex, 0);
        glBindBuffer(GL_ARRAY_BUFFER, vbo);
        glBufferData(GL_ARRAY_BUFFER, v.size() * sizeof(float), v.data(), GL_STREAM_DRAW);
        glEnableVertexAttribArray(aPos);
        glEnableVertexAttribArray(aUV);
        glEnableVertexAttribArray(aCol);
        glVertexAttribPointer(aPos, 2, GL_FLOAT, GL_FALSE, 32, (void*)0);
        glVertexAttribPointer(aUV, 2, GL_FLOAT, GL_FALSE, 32, (void*)8);
        glVertexAttribPointer(aCol, 4, GL_FLOAT, GL_FALSE, 32, (void*)16);
        glDrawArrays(GL_TRIANGLES, 0, (GLsizei)(v.size() / 8));
        glDisableVertexAttribArray(aPos);
        glDisableVertexAttribArray(aUV);
        glDisableVertexAttribArray(aCol);
        glDisable(GL_BLEND);
        glEnable(GL_DEPTH_TEST);
    }
};
